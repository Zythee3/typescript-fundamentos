var a = "a"   // por se tratar de uma declaração "global" ou seja pode ser alterada em qualquer lugar do código, não é muito recomendada
let b = "b"   // é uma variavel que pode ter o valor alterado futuramente em um local do arquivo onde foi definido
const c = 'c'  // é uma constante, ou seja ela não pode ser mudada ao longo do código, vai permanecer igual
